//   + slow / sits still / predictable         (perched raptor, roosting heron)
//   − tiny / hyperactive / never still        (kinglet, bushtit, gnatcatcher)
//   − stays in dense cover / canopy / reeds   (wrentit, marsh wren, vireo)
