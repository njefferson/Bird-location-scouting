// =============================================================================
// gen-county-shapes.mjs — regenerate src/data/county-shapes.js
// =============================================================================
// One-off dev tool (run from frame/): downloads a US-counties GeoJSON (FIPS-
// keyed), keeps the 58 California counties + the 5 Tahoe/Reno Nevada counties,
// projects them into a shared SVG viewBox (equirectangular, scaled for CA's
// mid-latitude) and writes SVG path data keyed by eBird region code (US-XX-###).
// Rerun only if you want to re-derive the map geometry; the output is committed.
//
//   node scripts/gen-county-shapes.mjs
// =============================================================================
import { writeFileSync } from 'node:fs';

const SRC = 'https://raw.githubusercontent.com/plotly/datasets/master/geojson-counties-fips.json';
const NV_WANTED = new Set(['005', '019', '029', '031', '510']); // Douglas, Lyon, Storey, Washoe, Carson City

function wanted(geoid) {
  const st = geoid.slice(0, 2), co = geoid.slice(2);
  if (st === '06') return `US-CA-${co}`;
  if (st === '32' && NV_WANTED.has(co)) return `US-NV-${co}`;
  return null;
}

// Equirectangular projection; longitude compressed by cos(lat) so proportions hold.
const MID_LAT = 38.5;
const kx = Math.cos((MID_LAT * Math.PI) / 180);
const projRaw = (lng, lat) => [lng * kx, -lat];

// Ramer–Douglas–Peucker line simplification on projected [x,y] rings.
function rdp(pts, eps) {
  if (pts.length < 3) return pts;
  let maxD = 0, idx = 0;
  const [ax, ay] = pts[0], [bx, by] = pts[pts.length - 1];
  const dx = bx - ax, dy = by - ay;
  const len2 = dx * dx + dy * dy || 1e-12;
  for (let i = 1; i < pts.length - 1; i++) {
    const [px, py] = pts[i];
    const t = ((px - ax) * dx + (py - ay) * dy) / len2;
    const cx = ax + t * dx, cy = ay + t * dy;
    const d = (px - cx) ** 2 + (py - cy) ** 2;
    if (d > maxD) { maxD = d; idx = i; }
  }
  if (maxD > eps * eps) return rdp(pts.slice(0, idx + 1), eps).slice(0, -1).concat(rdp(pts.slice(idx), eps));
  return [pts[0], pts[pts.length - 1]];
}
function ringArea(ring) {
  let a = 0;
  for (let i = 0; i < ring.length - 1; i++) a += ring[i][0] * ring[i + 1][1] - ring[i + 1][0] * ring[i][1];
  return Math.abs(a) / 2;
}

const res = await fetch(SRC);
if (!res.ok) { console.error(`Fetch failed: ${res.status}`); process.exit(1); }
const gj = await res.json();

const feats = {};
for (const f of gj.features) {
  const code = wanted(String(f.id));
  if (!code) continue;
  const polys = f.geometry.type === 'Polygon' ? [f.geometry.coordinates] : f.geometry.coordinates;
  feats[code] = polys.map((poly) => poly.map((ring) => ring.map(([lng, lat]) => projRaw(lng, lat))));
}

let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
for (const code in feats) for (const poly of feats[code]) for (const ring of poly) for (const [x, y] of ring) {
  if (x < minX) minX = x; if (x > maxX) maxX = x;
  if (y < minY) minY = y; if (y > maxY) maxY = y;
}
const W = 1000;
const scale = W / (maxX - minX);
const H = Math.round((maxY - minY) * scale);
const fit = ([x, y]) => [(x - minX) * scale, (y - minY) * scale];

const EPS = 0.9, MIN_AREA = 4;
const shapes = {};
let totalPts = 0;
for (const code in feats) {
  const parts = [];
  for (const poly of feats[code]) for (const ring of poly) {
    const fitted = rdp(ring.map(fit), EPS);
    if (fitted.length < 4 || ringArea(fitted) < MIN_AREA) continue;
    parts.push(fitted.map(([x, y], i) => `${i ? 'L' : 'M'}${x.toFixed(1)} ${y.toFixed(1)}`).join('') + 'Z');
    totalPts += fitted.length;
  }
  shapes[code] = parts.join('');
}

const codes = Object.keys(shapes).sort();
const banner = `// AUTO-GENERATED by scripts/gen-county-shapes.mjs — do not hand-edit.
// SVG path data for the 58 California counties + the 5 Tahoe/Reno Nevada
// counties, projected into a shared ${W}×${H} viewBox (equirectangular, scaled
// for CA's mid-latitude). Keyed by eBird region code (US-XX-###), matching
// COUNTIES in counties.js and the per-county data files. Consumed by the
// county-picker map (ui/regionpicker.js). ${codes.length} counties, ~${totalPts} points.\n`;
const body = codes.map((c) => `  '${c}': '${shapes[c]}',`).join('\n');
writeFileSync('src/data/county-shapes.js',
  `${banner}export const MAP_VIEWBOX = { w: ${W}, h: ${H} };\n\nexport const COUNTY_SHAPES = {\n${body}\n};\n`);
console.log(`Wrote src/data/county-shapes.js — ${codes.length} counties, ${totalPts} points, viewBox ${W}x${H}`);
