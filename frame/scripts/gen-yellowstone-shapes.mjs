// =============================================================================
// gen-yellowstone-shapes.mjs — regenerate src/data/yellowstone-shapes.js
// =============================================================================
// Sibling of gen-county-shapes.mjs for the GREATER-YELLOWSTONE map area (v38):
// the 5 WY/MT/ID counties around the park, projected into their OWN viewBox
// with their OWN mid-latitude (the California projection is fitted to CA and
// would put these far off-canvas — see src/data/map-areas.js for the area
// concept). Same source, same simplification, same output shape as the CA
// generator; output is committed and shipped offline.
//
//   node scripts/gen-yellowstone-shapes.mjs      (run from frame/)
// =============================================================================
import { writeFileSync } from 'node:fs';

const SRC = 'https://raw.githubusercontent.com/plotly/datasets/master/geojson-counties-fips.json';

// FIPS → eBird region code for the Greater-Yellowstone five.
const WANTED = {
  '56029': 'US-WY-029', // Park (WY) — Mammoth, Lamar, Canyon, Lake
  '56039': 'US-WY-039', // Teton (WY) — Yellowstone S + Grand Teton NP, Jackson
  '30031': 'US-MT-031', // Gallatin (MT) — West Yellowstone entrance
  '30067': 'US-MT-067', // Park (MT) — Gardiner / north entrance
  '16043': 'US-ID-043', // Fremont (ID) — Island Park / Bechler corner
};

// Equirectangular projection at the area's own mid-latitude.
const MID_LAT = 44.6;
const kx = Math.cos((MID_LAT * Math.PI) / 180);
const projRaw = (lng, lat) => [lng * kx, -lat];

// Ramer–Douglas–Peucker line simplification on projected [x,y] rings.
function rdp(pts, eps) {
  if (pts.length < 3) return pts;
  let maxD = 0, idx = 0;
  const [ax, ay] = pts[0], [bx, by] = pts[pts.length - 1];
  const dx = bx - ax, dy = by - ay;
  const len2 = dx * dx + dy * dy || 1e-12;
  for (let i = 1; i < pts.length - 1; i++) {
    const [px, py] = pts[i];
    const t = ((px - ax) * dx + (py - ay) * dy) / len2;
    const cx = ax + t * dx, cy = ay + t * dy;
    const d = (px - cx) ** 2 + (py - cy) ** 2;
    if (d > maxD) { maxD = d; idx = i; }
  }
  if (maxD > eps * eps) return rdp(pts.slice(0, idx + 1), eps).slice(0, -1).concat(rdp(pts.slice(idx), eps));
  return [pts[0], pts[pts.length - 1]];
}
function ringArea(ring) {
  let a = 0;
  for (let i = 0; i < ring.length - 1; i++) a += ring[i][0] * ring[i + 1][1] - ring[i + 1][0] * ring[i][1];
  return Math.abs(a) / 2;
}

const res = await fetch(SRC);
if (!res.ok) { console.error(`Fetch failed: ${res.status}`); process.exit(1); }
const gj = await res.json();

const feats = {};
for (const f of gj.features) {
  const code = WANTED[String(f.id)];
  if (!code) continue;
  const polys = f.geometry.type === 'Polygon' ? [f.geometry.coordinates] : f.geometry.coordinates;
  feats[code] = polys.map((poly) => poly.map((ring) => ring.map(([lng, lat]) => projRaw(lng, lat))));
}
const missing = Object.values(WANTED).filter((c) => !feats[c]);
if (missing.length) { console.error(`Counties missing from source: ${missing.join(', ')}`); process.exit(1); }

let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
for (const code in feats) for (const poly of feats[code]) for (const ring of poly) for (const [x, y] of ring) {
  if (x < minX) minX = x; if (x > maxX) maxX = x;
  if (y < minY) minY = y; if (y > maxY) maxY = y;
}
const W = 1000;
const scale = W / (maxX - minX);
const H = Math.round((maxY - minY) * scale);
const fit = ([x, y]) => [(x - minX) * scale, (y - minY) * scale];

const EPS = 0.9, MIN_AREA = 4;
const shapes = {};
let totalPts = 0;
for (const code in feats) {
  const parts = [];
  for (const poly of feats[code]) for (const ring of poly) {
    const fitted = rdp(ring.map(fit), EPS);
    if (fitted.length < 4 || ringArea(fitted) < MIN_AREA) continue;
    parts.push(fitted.map(([x, y], i) => `${i ? 'L' : 'M'}${x.toFixed(1)} ${y.toFixed(1)}`).join('') + 'Z');
    totalPts += fitted.length;
  }
  shapes[code] = parts.join('');
}

const codes = Object.keys(shapes).sort();
const banner = `// AUTO-GENERATED by scripts/gen-yellowstone-shapes.mjs — do not hand-edit.
// SVG path data for the 5 Greater-Yellowstone counties (Park & Teton WY,
// Gallatin & Park MT, Fremont ID), projected into their OWN ${W}×${H} viewBox
// (equirectangular at mid-lat ${MID_LAT} — NOT the California projection).
// Keyed by eBird region code, matching COUNTIES in counties.js and the
// per-county data files. Consumed via src/data/map-areas.js.
//   x = (lng·kx − minX)·scale,  y = (−lat − minY)·scale
// ${codes.length} counties, ~${totalPts} points.\n`;
const body = codes.map((c) => `  '${c}': '${shapes[c]}',`).join('\n');
writeFileSync('src/data/yellowstone-shapes.js',
  `${banner}export const YS_VIEWBOX = { w: ${W}, h: ${H} };\n\n` +
  `export const YS_PROJECTION = { kx: ${kx}, minX: ${minX}, minY: ${minY}, scale: ${scale} };\n\n` +
  `export const YS_SHAPES = {\n${body}\n};\n`);
console.log(`Wrote src/data/yellowstone-shapes.js — ${codes.length} counties, ${totalPts} points, viewBox ${W}x${H}`);
